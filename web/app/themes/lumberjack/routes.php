<?php

use Rareloop\Lumberjack\Facades\Router;
use Zend\Diactoros\Response\RedirectResponse;
use Rareloop\Lumberjack\Post;

Router::get('/most-recent', function () {
    $posts = Post::builder()
        ->orderBy('date', 'asc')
        ->limit(1)
        ->get();

    return new RedirectResponse($posts[0]->link);
});
